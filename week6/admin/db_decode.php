        "SELECT r.*, u.full_name, u.role
FROM replies r
JOIN users u ON r.user_id = u.id
WHERE r.ticket_id = ?
ORDER BY r.created_at ASC;",
        "<b>ORDER BY created_at ASC</b> — oldest reply first (chronological chat order), opposite of ticket listing.<br>
         <b>u.role</b> — used by the UI to decide whether to label a reply 'IT Support 🛠️' or 'User 👤'.",
        'ticket.php'
    );

    decode_block(
        'Insert a new reply',
        "INSERT INTO replies (ticket_id, user_id, message, is_admin)
VALUES (?, ?, ?, ?);
-- PHP: bind_param(\"iisi\", \$ticket_id, \$user_id, \$message, \$is_admin)
-- \$is_admin = isAdmin() ? 1 : 0",
        "<b>is_admin flag</b> — set to 1 if the reply is from an admin; this switches the UI to the blue 'IT Support' style.<br>
         <b>Auto-status update after admin reply</b>: If the ticket is still 'Pending' when an admin replies, a second query fires:<br>
         <code>UPDATE tickets SET status='In Progress' WHERE id=\$ticket_id</code><br>
         This automatically moves the ticket forward in the workflow.",
        'ticket.php'
